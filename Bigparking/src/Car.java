public class Car {
        
    private String name;
    private int numberCar;

    public Car(String name, int numberCar) {
        this.name = name;
        this.numberCar = numberCar;
    }

    public String getName() {
        return name;
    }

    public int getNumberCar() {
        return numberCar;
    }
    
}
